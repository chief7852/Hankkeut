package vo;

public class CommentVO {
		
	private String mem_id        ;
	private String co_date       ;
	private String co_contents   ;
	private String board_no      ;
	private int co_no         ;
	
	
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getCo_date() {
		return co_date;
	}
	public void setCo_date(String co_date) {
		this.co_date = co_date;
	}
	public String getCo_contents() {
		return co_contents;
	}
	public void setCo_contents(String co_contents) {
		this.co_contents = co_contents;
	}
	public String getBoard_no() {
		return board_no;
	}
	public void setBoard_no(String board_no) {
		this.board_no = board_no;
	}
	public int getCo_no() {
		return co_no;
	}
	public void setCo_no(int co_no) {
		this.co_no = co_no;
	}

}
